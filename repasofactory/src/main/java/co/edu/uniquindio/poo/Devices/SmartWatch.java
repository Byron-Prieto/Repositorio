package co.edu.uniquindio.poo.Devices;

public class SmartWatch{
    
    int pantalla;
    String color;
    int almacenamiento;
    int bateria; 

    public SmartWatch(int pantalla, int almacenamiento, int bateria, String color){

        this.pantalla = pantalla;
        this.color = color;
        this.almacenamiento = almacenamiento;
        this.bateria = bateria;
    }
}
